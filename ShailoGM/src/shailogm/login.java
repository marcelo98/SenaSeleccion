    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        char contrasenaEncriptada[];
        contrasenaEncriptada = jPasswordField1.getPassword();
        String cont = "";

        for (char s : contrasenaEncriptada) {
            cont = cont + s;
        }

        if (usuario.getText().equals("") || contrasenaEncriptada.length == 0) {
            JOptionPane.showMessageDialog(rootPane, "porfavor llene todos los campos");
        } else {
            int retorno = manejo.ValidarUsuario(usuario.getText(), cont);

            if (retorno == 1) {
                JOptionPane.showMessageDialog(rootPane, "validacion correcta");
                Menu men = new Menu();
                men.setVisible(true);
                this.dispose();

            } else if (retorno == 2) {
                JOptionPane.showMessageDialog(rootPane, "validacion incorrecta");
            } else {
                JOptionPane.showMessageDialog(rootPane, "error al leeer el archivo");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

